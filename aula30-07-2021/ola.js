// criando uma variável do tipo string
var x = 'Olá!';
console.log("Texto: " + x + ", tipo de dado: " + typeof x);

// alterando a variável para int
x = 12 + 13;
console.log("Número inteiro: " + x + ", tipo de dado : " + typeof x);

// alterando a variável para float
x = x + 5.6;
console.log("Número real: " + x + ", tipo de dado : " + typeof x);

// alterando a variável para int novamente através do parseInt
console.log("Número inteiro novamente: " + parseInt(x) + ", tipo de dado : " + typeof x);

// alterando a variável para int novamente através do parseInt
console.log("Número float novamente: " + parseFloat(x) + ", tipo de dado : " + typeof x);

// alterando a variável para booleano
x = false;
console.log("Lógico: " + x + ", tipo de dado: " + typeof x);